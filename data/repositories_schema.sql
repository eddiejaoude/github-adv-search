DROP TABLE IF EXISTS `repositories`;
CREATE TABLE `repositories` (
  `id` int(10) unsigned NOT NULL AUTO_INCREMENT,
  `search_id` int(10) unsigned NOT NULL,
  `type` varchar(64) DEFAULT NULL,
  `username` varchar(128) DEFAULT NULL,
  `pushed` datetime DEFAULT NULL,
  `has_wiki` tinyint(1) DEFAULT NULL,
  `watchers` int(10) unsigned NOT NULL,
  `description` text DEFAULT NULL,
  `open_issues` int(10) unsigned NOT NULL,
  `language` varchar(32) DEFAULT NULL,
  `fork` tinyint(1) DEFAULT NULL,
  `has_issues` tinyint(1) DEFAULT NULL,
  `homepage` varchar(255) DEFAULT NULL,
  `size` int(10) unsigned NOT NULL,
  `private` tinyint(1) DEFAULT NULL,
  `followers` int(10) unsigned NOT NULL,
  `name` varchar(255) DEFAULT NULL,
  `owner` varchar(128) DEFAULT NULL,
  `has_downloads` tinyint(1) DEFAULT NULL,
  `pushed_at` datetime DEFAULT NULL,
  `score` float(12,8) DEFAULT NULL,
  `url` varchar(255) DEFAULT NULL,
  `forks` int(10) unsigned NOT NULL,
  `created` datetime DEFAULT NULL,
  `created_at` datetime DEFAULT NULL,
  `updated_at` datetime DEFAULT NULL,
  PRIMARY KEY (`id`)
) ENGINE=InnoDB AUTO_INCREMENT=1 DEFAULT CHARSET=utf8;
